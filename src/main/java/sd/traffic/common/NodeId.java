package sd.traffic.common;

/**
 * Enumeração que define todos os nós (identificadores únicos)
 * da rede de tráfego simulada.
 *
 * Inclui três entradas (E1–E3), cinco cruzamentos (Cr1–Cr5)
 * e uma saída (S).
 *
 * Estes IDs são usados por veículos, cruzamentos e mensagens
 * para indicar a posição e destino de cada entidade.
 */
public enum NodeId {
    E1, E2, E3, Cr1, Cr2, Cr3, Cr4, Cr5, S
}
